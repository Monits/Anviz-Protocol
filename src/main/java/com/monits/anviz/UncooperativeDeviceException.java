package com.monits.anviz;

public class UncooperativeDeviceException extends Exception {

	public UncooperativeDeviceException() {
		super();
	}

	public UncooperativeDeviceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UncooperativeDeviceException(String arg0) {
		super(arg0);
	}

	public UncooperativeDeviceException(Throwable arg0) {
		super(arg0);
	}
	
}
